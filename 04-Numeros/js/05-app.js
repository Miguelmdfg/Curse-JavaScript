//Incrementos o decrementos

//Incremento

let puntaje = 5;

puntaje++; //esto al ejecutarlo retornará 5 y al volverlo a ejecutar retornará 6
++puntaje; //Al ejecutarlo sumara un numero inmediatamente, es decir, en este caso retornará 6 de una vez

console.log(puntaje);

//Decremento

let puntaje2 = 4;

puntaje--; //Esto al ejecutarlo retornará 4 y al volverlo a ejecutar retornará 3
--puntaje //Al ejecutarlo restará un numero inmediatamente, es decir, en este caso retornará 3 de una vez

//Aumento de varios numeros

let puntaje3 = 6;

puntaje += 3; //Ponemos el numero que queremos que se sumen
puntaje -= 3; //Ponemos el numero que queremos que se reste

