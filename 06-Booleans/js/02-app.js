//Comparar Booleans

const boolean1 = true;
const boolean2 = false;


console.log(boolean1 === boolean2); //Son el mismo tipo de dato pero no son el mismo valor

console.log(boolean1 === true); //Devuelve true porque efectivamente son el mismo tipo de dato
console.log(boolean1 === "true"); //Devuelve false porque esto es un string, no es exactamente el mismo tipo de dato 

