//Crear Arrays en JS

// Para lo que sirven los arreglos es para agrupar elementos de un mismo tipo
// Mientras que un objeto va a ser una variable que va a agrupar la informacion de un elemento de un objeto, un arreglo puede tener multiples elementos que sean iguales
// Los Objetos se crean con llaves '{}' mientras que los arrays se crean con corchetes '[]'. Por ejemplo:

const numeros = [10, 20, 30 ]; //Esta es la forma mas común de crear un array, también hay otra manera que veremos a continuación:

const meses = new Array (Enero, Febrero, Marzo); //Esta forma no es muy común pero igual es bueno conocerla

console.log(numeros);
console.log(meses);


//Un arreglo que contiene datos de todo tipo

